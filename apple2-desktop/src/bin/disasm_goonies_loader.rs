fn format_operand(mode: &str, op1: u8, op2: u8, pc: usize) -> String {
    match mode {
        "impl" | "A" => String::new(),
        "#" => format!("#${:02X}", op1),
        "zpg" => format!("${:02X}", op1),
        "zpg,X" => format!("${:02X},X", op1),
        "zpg,Y" => format!("${:02X},Y", op1),
        "abs" => format!("${:04X}", op1 as u16 | (op2 as u16) << 8),
        "abs,X" => format!("${:04X},X", op1 as u16 | (op2 as u16) << 8),
        "abs,Y" => format!("${:04X},Y", op1 as u16 | (op2 as u16) << 8),
        "(ind,X)" => format!("(${:02X},X)", op1),
        "(ind),Y" => format!("(${:02X}),Y", op1),
        "rel" => format!("${:04X}", (pc as i32 + 2 + op1 as i8 as i32) as u16),
        "(ind)" => format!("(${:04X})", op1 as u16 | (op2 as u16) << 8),
        _ => "???".to_string(),
    }
}

fn decode_6502(opcode: u8) -> (&'static str, &'static str, usize) {
    match opcode {
        0x00 => ("BRK", "impl", 1),
        0x08 => ("PHP", "impl", 1),
        0x09 => ("ORA", "#", 2),
        0x0A => ("ASL", "A", 1),
        0x0B => ("???", "impl", 1),
        0x0D => ("ORA", "abs", 3),
        0x10 => ("BPL", "rel", 2),
        0x18 => ("CLC", "impl", 1),
        0x20 => ("JSR", "abs", 3),
        0x24 => ("BIT", "zpg", 2),
        0x28 => ("PLP", "impl", 1),
        0x29 => ("AND", "#", 2),
        0x2A => ("ROL", "A", 1),
        0x2C => ("BIT", "abs", 3),
        0x30 => ("BMI", "rel", 2),
        0x38 => ("SEC", "impl", 1),
        0x48 => ("PHA", "impl", 1),
        0x4A => ("LSR", "A", 1),
        0x4C => ("JMP", "abs", 3),
        0x4E => ("LSR", "abs", 3),
        0x60 => ("RTS", "impl", 1),
        0x66 => ("ROR", "zpg", 2),
        0x68 => ("PLA", "impl", 1),
        0x6A => ("ROR", "A", 1),
        0x6C => ("JMP", "(ind)", 3),
        0x70 => ("BVS", "rel", 2),
        0x8C => ("STY", "abs", 3),
        0x8D => ("STA", "abs", 3),
        0x90 => ("BCC", "rel", 2),
        0x98 => ("TYA", "impl", 1),
        0x99 => ("STA", "abs,Y", 3),
        0xA0 => ("LDY", "#", 2),
        0xA2 => ("LDX", "#", 2),
        0xA4 => ("LDY", "zpg", 2),
        0xA5 => ("LDA", "zpg", 2),
        0xA8 => ("TAY", "impl", 1),
        0xA9 => ("LDA", "#", 2),
        0xAA => ("TAX", "impl", 1),
        0xAC => ("LDY", "abs", 3),
        0xAD => ("LDA", "abs", 3),
        0xAE => ("LDX", "abs", 3),
        0xB0 => ("BCS", "rel", 2),
        0xB1 => ("LDA", "(ind),Y", 2),
        0xB9 => ("LDA", "abs,Y", 3),
        0xBD => ("LDA", "abs,X", 3),
        0xC5 => ("CMP", "zpg", 2),
        0xC8 => ("INY", "impl", 1),
        0xCA => ("DEX", "impl", 1),
        0xCC => ("CPY", "abs", 3),
        0xCD => ("CMP", "abs", 3),
        0xCE => ("DEC", "abs", 3),
        0xD0 => ("BNE", "rel", 2),
        0xD4 => ("???", "zpg,X", 2),
        0xE6 => ("INC", "zpg", 2),
        0xE8 => ("INX", "impl", 1),
        0xEA => ("NOP", "impl", 1),
        0xF0 => ("BEQ", "rel", 2),
        _ => ("???", "impl", 1),
    }
}

